a =input('Digite algo ?')
print('Tipo primitivo desse valor é  ?',type(a))
print('Só tem espaço ?', a.isspace())
print('È um numero ?' , a.isnumeric())
print('é alfabético ?', a.isalpha())
print('È alfanumérico?', a.isalnum())
print('Está em letra maiúscula ?', a.isupper())
print('Está em minúscula ?', a.islower())
print('Èssa palavra esta capitalizada', a.istitle())





